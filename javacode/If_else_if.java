package javacode;

public class If_else_if {

	public static void main(String[] args) {

		double marks= 49;

		if (marks >= 90) {
			System.out.println("Grade: A+");
		} else

		if (marks >= 80) {
			System.out.println("Grade: A");
		} else

		if (marks >= 70) {
			System.out.println("Grade: B");
		} else

		if (marks >= 60) {
			System.out.println("Grade: C");
		} else

		if (marks >= 50) {
			System.out.println("Grade: D");
		} else {
			System.out.println("Grade: FAIL");
		}
	}
}
