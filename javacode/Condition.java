package javacode;

public class Condition {

	public static void main(String[] args) {
		int a = 10;
		int b = 21;
		if (a > b)// false
		{
			System.out.println("1");
		}
		if (a < b)// true
		{
			System.out.println("2");
		}
		if (a == b)// false
		{
			System.out.println("3");
		}
		if (a >= b)// false
		{
			System.out.println("4");
		}
		if (a <= b)// true
		{
			System.out.println("5");
		}
		if (a != b)// true
		{
			System.out.println("6");
		}

	}

}
